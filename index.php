            <h1>Dispositivo Portátil de Portales de Aperture Science</h1>
            <div align=center><img src="imagen/pistola.jpg" alt=""  /></div>
            <p>El Dispositivo Portátil de Portales de Aperture Science,<br>
                también conocido comúnmente como Pistola de Portales o por<br>
                sus siglas en inglés, "ASHPD" (Aperture Science Handheld <br>               
                Portal Device), es una herramienta experimental utilizada <br>
                para crear dos portales a través de la cual los objetos <br>
                pueden pasar. Se utiliza en las cámaras de prueba del Centro<br>
                de Desarrollo de Aperture Science, es la herramienta principal <br>
               que el Sujeto de Prueba debe utilizar para completar cada prueba.
            </p>
            <p>
                La Pistola de Portales es un dispositivo que tiene la capacidad <br>
                de fabricar dos portales enlazados. No importa la distancia entre <br>
                ellos, cualquier objeto que pase a través de un portal saldrá del <br>
                otro y viceversa instantáneamente. Los portales se pueden colocar <br>
                en cualquier superficie que sea manufacturada o refinada con roca <br>
                lunar y sea lo suficientemente grande para acomodarse a ella. Objetos,<br>
                o ciertos tipos de superficies en movimiento, generalmente previenen la <br>
                formación de un portal. La Red de Emancipación Material de Aperture <br>
                Science bloqueará cualquier intento de disparar portales a través <br>
                de ella, así como desactivara los portales activos si el ASHPD pasa <br>
                a través de élla.
            </p>
            
